#!/usr/bin/env bash

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

TOOL="$(basename "$0" ".sh" | sed -E 's/^go-//g')"

set -e

die() {
	echo >&2 "$@"
	exit 1
}

RACE="${RACE:-false}"
REPO_ROOT="${SCRIPT_DIR}/.."

# Read version data directly from committed files and git.
COLLECTOR_VERSION="$(cat "${REPO_ROOT}/COLLECTOR_VERSION")" || die "Missing COLLECTOR_VERSION"
SCANNER_VERSION="$(cat "${REPO_ROOT}/SCANNER_VERSION")" || die "Missing SCANNER_VERSION"
FACT_VERSION="$(cat "${REPO_ROOT}/FACT_VERSION")" || die "Missing FACT_VERSION"

# Generate version data file. Tests use only the base tag (stable across
# commits) to keep ActionIDs stable for test result caching. Builds get
# the full git-describe version with commit count and SHA.
generate_version_file() {
	local target="${REPO_ROOT}/pkg/version/internal/zversion.go"
	local main_version git_short_sha

	local base_tag git_short_sha commit_count
	base_tag="$(cd "${REPO_ROOT}"; git describe --tags --abbrev=0 --exclude '*-nightly-*' 2>/dev/null || echo "")"
	git_short_sha="$(cd "${REPO_ROOT}"; git rev-parse --short HEAD 2>/dev/null || echo "")"
	commit_count="$(cd "${REPO_ROOT}"; git rev-list --count "${base_tag}..HEAD" 2>/dev/null || echo "0")"

	if [[ "$TOOL" == "test" ]]; then
		# Base tag only (e.g. "4.11.x") — stable across commits.
		main_version="${base_tag}"
		git_short_sha=""
	else
		# Full format matching git describe (e.g. "4.11.x-193-g7257553280").
		main_version="${base_tag}-${commit_count}-g${git_short_sha}"
	fi

	local new_content
	new_content="// Code generated by go-tool.sh; DO NOT EDIT.

package internal

func init() {
	MainVersion = \"${main_version}\"
	CollectorVersion = \"${COLLECTOR_VERSION}\"
	FactVersion = \"${FACT_VERSION}\"
	ScannerVersion = \"${SCANNER_VERSION}\"
	GitShortSha = \"${git_short_sha}\"
}"
	if [[ -f "$target" ]] && [[ "$(cat "$target")" == "$new_content" ]]; then
		return
	fi
	echo "$new_content" > "$target"
}
generate_version_file

ldflags=()
if [[ "$DEBUG_BUILD" != "yes" ]]; then
  ldflags+=(-s -w)
fi

if [[ "${CGO_ENABLED}" != 0 ]]; then
  echo >&2 "CGO_ENABLED is not 0. Compiling with -linkmode=external"
  ldflags+=('-linkmode=external')
fi

function invoke_go() {
  tool="$1"
  shift
  if [[ "$RACE" == "true" ]]; then
    CGO_ENABLED=1 go "$tool" -race -ldflags="${ldflags[*]}" -tags "$(tr , ' ' <<<"$GOTAGS")" "$@"
  else
    go "$tool" -ldflags="${ldflags[*]}" -tags "$(tr , ' ' <<<"$GOTAGS")" "$@"
  fi
}

function go_build() (
  export GOOS="${GOOS:-${DEFAULT_GOOS}}"
  [[ -n "$GOOS" ]] || die "GOOS must be set"
  [[ -n "$GOARCH" ]] || die "GOARCH must be set"

  dirs=()
  for main_srcdir in "$@"; do
    if ! [[ "$main_srcdir" =~ ^\.?/ ]]; then
      main_srcdir="./$main_srcdir"
    fi
    dirs+=("$main_srcdir")
  done

  output="bin/${GOOS}_${GOARCH}"
  mkdir -p "$output"

  echo >&2 "Compiling Go source in ${dirs[*]} to ${output}"
  local total
  total=$(go list -deps "${dirs[@]}" 2>/dev/null | wc -l | tr -d ' ')
  local compiled_count=/tmp/go-build-compiled-count
  invoke_go_build -o "$output" "${dirs[@]}" 2> >(tee >(wc -l | tr -d ' ' > "$compiled_count") >&2)
  wait
  local compiled
  compiled=$(cat "$compiled_count" 2>/dev/null | tr -d ' ')
  echo >&2 "Build cache: $((total - compiled))/$total hits ($compiled compiled)"
)

function go_build_file() {
    local input_file="$1"
    local output_file="$2"
    invoke_go_build -o "${output_file}" "${input_file}"
}

function invoke_go_build() {
  local gcflags=()
  if [[ "$DEBUG_BUILD" == "yes" ]]; then
    gcflags+=(-gcflags "all=-N -l")
  fi
  invoke_go build -v -trimpath -buildvcs=false "${gcflags[@]}" "$@"
}

function go_run() (
  invoke_go run "$@"
)

function go_test() (
  unset GOOS
  # Count test compilation cache hits via a dry-run compile.
  # Extract package paths from args (skip flags and flag values).
  local pkgs=()
  local skip_next=false
  for arg in "$@"; do
    if $skip_next; then skip_next=false; continue; fi
    case "$arg" in
      -coverprofile|-covermode|-coverpkg|-outputdir|-o|-timeout|-p|-parallel|-run|-bench|-benchtime|-count|-cpu|-list|-memprofile|-cpuprofile|-blockprofile|-mutexprofile|-trace|-shuffle)
        skip_next=true ;;
      -*) ;;
      *) pkgs+=("$arg") ;;
    esac
  done
  if [[ ${#pkgs[@]} -gt 0 ]]; then
    local race_flag=""
    [[ "$RACE" == "true" ]] && race_flag="-race"
    local compiled
    compiled=$(go test -c -x -buildvcs=false $race_flag -ldflags="${ldflags[*]}" -tags "$(tr , ' ' <<<"$GOTAGS")" -o /dev/null "${pkgs[@]}" 2>&1 | grep -c '/compile ' || true)
    if [[ "$compiled" -eq 0 ]]; then
      echo >&2 "Test build cache: fully cached"
    else
      echo >&2 "Test build cache: $compiled packages compiled"
    fi
  fi
  GODEBUG="${GODEBUG:+$GODEBUG,}gocachetest=1" invoke_go test -buildvcs=false "$@"
)

case "$TOOL" in
  build)
    go_build "$@"
    ;;
  build-file)
    go_build_file "$1" "$2"
    ;;
  test)
    go_test "$@"
    ;;
  run)
    go_run "$@"
    ;;
  *)
    die "Unknown go tool '${TOOL}'"
    ;;
esac
