#!/usr/bin/env bash

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

TOOL="$(basename "$0" ".sh" | sed -E 's/^go-//g')"

set -e

die() {
	echo >&2 "$@"
	exit 1
}

RACE="${RACE:-false}"

# Parse version info from status.sh.
while read -r line || [[ -n "$line" ]]; do
	if [[ "$line" =~ ^[[:space:]]*$ ]]; then
		continue
	elif [[ "$line" =~ ^([^[:space:]]+)[[:space:]]+(.*)[[:space:]]*$ ]]; then
		var="${BASH_REMATCH[1]}"
		def="${BASH_REMATCH[2]}"
		eval "status_${var}=$(printf '%q' "$def")"
	else
		die "Malformed status.sh output line ${line}"
	fi
done < <(cd "${SCRIPT_DIR}/.."; ./status.sh)

# Generate version data file (like Go's own zbootstrap.go pattern).
# This replaces the previous //XDef: + ldflags mechanism. By writing version
# info as Go source instead of passing it via -X ldflags, we avoid invalidating
# the Go build/link cache when only the version string changes.
# The file is only written when content changes (skip-if-same).
generate_version_file() {
	local target="${SCRIPT_DIR}/../pkg/version/internal/zversion.go"
	local var
	for var in status_STABLE_MAIN_VERSION status_STABLE_COLLECTOR_VERSION \
	           status_STABLE_FACT_VERSION status_STABLE_SCANNER_VERSION \
	           status_STABLE_GIT_SHORT_SHA; do
		[[ -n "${!var}" ]] || die "${var#status_} is not set in status.sh output"
	done
	local new_content
	new_content="// Code generated by go-tool.sh; DO NOT EDIT.

package internal

func init() {
	MainVersion = \"${status_STABLE_MAIN_VERSION}\"
	CollectorVersion = \"${status_STABLE_COLLECTOR_VERSION}\"
	FactVersion = \"${status_STABLE_FACT_VERSION}\"
	ScannerVersion = \"${status_STABLE_SCANNER_VERSION}\"
	GitShortSha = \"${status_STABLE_GIT_SHORT_SHA}\"
}"
	if [[ -f "$target" ]] && [[ "$(cat "$target")" == "$new_content" ]]; then
		return
	fi
	echo "$new_content" > "$target"
}
generate_version_file

ldflags=()
if [[ "$DEBUG_BUILD" != "yes" ]]; then
  ldflags+=(-s -w)
fi

if [[ "${CGO_ENABLED}" != 0 ]]; then
  echo >&2 "CGO_ENABLED is not 0. Compiling with -linkmode=external"
  ldflags+=('-linkmode=external')
fi

function invoke_go() {
  tool="$1"
  shift
  if [[ "$RACE" == "true" ]]; then
    CGO_ENABLED=1 go "$tool" -race -ldflags="${ldflags[*]}" -tags "$(tr , ' ' <<<"$GOTAGS")" "$@"
  else
    go "$tool" -ldflags="${ldflags[*]}" -tags "$(tr , ' ' <<<"$GOTAGS")" "$@"
  fi
}

function go_build() (
  export GOOS="${GOOS:-${DEFAULT_GOOS}}"
  [[ -n "$GOOS" ]] || die "GOOS must be set"
  [[ -n "$GOARCH" ]] || die "GOARCH must be set"

  dirs=()
  for main_srcdir in "$@"; do
    if ! [[ "$main_srcdir" =~ ^\.?/ ]]; then
      main_srcdir="./$main_srcdir"
    fi
    dirs+=("$main_srcdir")
  done

  output="bin/${GOOS}_${GOARCH}"
  mkdir -p "$output"

  echo >&2 "Compiling Go source in ${dirs[*]} to ${output}"
  invoke_go_build -o "$output" "${dirs[@]}"
)

function go_build_file() {
    local input_file="$1"
    local output_file="$2"
    invoke_go_build -o "${output_file}" "${input_file}"
}

function invoke_go_build() {
  local gcflags=()
  if [[ "$DEBUG_BUILD" == "yes" ]]; then
    gcflags+=(-gcflags "all=-N -l")
  fi
  invoke_go build -v -trimpath -buildvcs=false "${gcflags[@]}" "$@"
}

function go_run() (
  invoke_go run "$@"
)

function go_test() (
  unset GOOS
  invoke_go test -buildvcs=false "$@"
)

case "$TOOL" in
  build)
    go_build "$@"
    ;;
  build-file)
    go_build_file "$1" "$2"
    ;;
  test)
    go_test "$@"
    ;;
  run)
    go_run "$@"
    ;;
  *)
    die "Unknown go tool '${TOOL}'"
    ;;
esac
