package main

import (
	"fmt"
	"os"
	"path"
	"regexp"
	"strconv"

	_ "embed"

	"github.com/spf13/cobra"
	bc "github.com/stackrox/rox/tools/generate-helpers/bootstrap-common"
)

//go:embed migration.go.tpl
var migrationFile string

//go:embed migration_impl.go.tpl
var migrationImplFile string

//go:embed migration_test.go.tpl
var migrationTestFile string

//go:embed seq_num.go.tpl
var seqNumFile string

const (
	autogenerated      = "// Code generated by make bootstrap_background_migration generator. DO NOT EDIT."
	registrationPrefix = "github.com/stackrox/rox/central/backgroundmigrations/migrations"
)

var (
	migrationTemplate     = bc.NewTemplate(autogenerated + migrationFile)
	migrationImplTemplate = bc.NewTemplate(migrationImplFile)
	migrationTestTemplate = bc.NewTemplate(migrationTestFile)
	seqNumTemplate        = bc.NewTemplate(autogenerated + seqNumFile)

	seqNumRegexp = regexp.MustCompile(`const CurrentBgMigrationSeqNum = (\d+)`)
)

func main() {
	c := &cobra.Command{
		Use: "bootstrap background migration",
	}
	var description string
	c.Flags().StringVar(&description, "description", "", "a sentence describing the migration")
	must(c.MarkFlagRequired("description"))
	var rootDirectory string
	c.Flags().StringVar(&rootDirectory, "root", "", "the root directory of the source tree")
	must(c.MarkFlagRequired("root"))

	c.RunE = func(*cobra.Command, []string) error {
		seqNumFilePath := path.Join(rootDirectory, "central", "backgroundmigrations", "seq_num.go")
		startVersion, err := readCurrentSeqNum(seqNumFilePath)
		if err != nil {
			return fmt.Errorf("reading current seq num: %w", err)
		}

		migrationDirName := bc.GetMigrationDirName(startVersion, description, true)

		templateMap := map[string]interface{}{
			"nextSeqNum":          startVersion + 1,
			"packageName":         bc.GetPackageName(startVersion),
			"startSequenceNumber": startVersion,
			"description":         description,
		}

		fullMigrationDirPath := path.Join(rootDirectory, "central", "backgroundmigrations", "migrations", migrationDirName)
		if err := os.MkdirAll(fullMigrationDirPath, 0755); err != nil {
			return err
		}

		if err := bc.RenderFile(templateMap, migrationTemplate, path.Join(fullMigrationDirPath, "migration.go")); err != nil {
			return err
		}
		if err := bc.RenderFile(templateMap, migrationImplTemplate, path.Join(fullMigrationDirPath, "migration_impl.go")); err != nil {
			return err
		}
		if err := bc.RenderFile(templateMap, migrationTestTemplate, path.Join(fullMigrationDirPath, "migration_test.go")); err != nil {
			return err
		}
		if err := bc.RenderFile(templateMap, seqNumTemplate, seqNumFilePath); err != nil {
			return err
		}

		registrationFilePath := path.Join(rootDirectory, "central", "backgroundmigrations", "runner", "all.go")
		return bc.RegisterMigration(registrationFilePath, migrationDirName, registrationPrefix)
	}

	if err := c.Execute(); err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}

func must(err error) {
	if err != nil {
		panic(err)
	}
}

func readCurrentSeqNum(filePath string) (int, error) {
	data, err := os.ReadFile(filePath)
	if err != nil {
		return 0, err
	}
	matches := seqNumRegexp.FindSubmatch(data)
	if matches == nil {
		return 0, fmt.Errorf("could not find CurrentBgMigrationSeqNum in %s", filePath)
	}
	return strconv.Atoi(string(matches[1]))
}
